#include <stdio.h>

void main() {
    char ch;
    int in;

    // Assigning a character to a char and an integer variable
    ch = 'A';
    in = 'A';
    printf("ch=%c\n", ch);
    printf("in=%d\n", in);
    printf("\n");

    // Assigning an integer to a char and an integer variable
    ch = 100;
    in = 100;
    printf("ch=%c\n", ch);
    printf("in=%d\n", in);
    printf("\n");

    // Assigning an integer to a char and an integer variable (2)
    ch = 65;
    in = 65;
    printf("ch=%c\n", ch);
    printf("in=%d\n", in);
    printf("\n");
}